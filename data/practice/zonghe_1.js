解析：①该题目考察的考点提炼：题目考察的是项目风险管理中针对威胁的风险应对策略。
②正确答案解析：
风险转移策略涉及将威胁的负面影响连同应对责任部分或全部转移给第三方，通常需支付一定成本。购买保险是将潜在经济损失转移给保险公司，使用履约保函是通过担保方承担合同履约风险，两者均属于典型的风险转移方式。该策略不消除风险，而是通过协议将风险后果转移给更专业的机构，从而降低自身承担的直接损失。
③其他答案不正确解析：
A（规避）：规避策略是通过改变项目计划、范围或方法，从根本上消除风险或其根源，例如采用更成熟技术或调整时间表。题目中承建方并未消除风险本身，而是通过外部工具转移风险后果，因此不属于规避。
C（接受）：接受策略分为被动接受（不采取行动）和主动接受（建立应急储备）。题目中承建方主动采取了购买保险和履约保函等积极措施，旨在转移风险而非接受风险，故不符合接受策略的定义。
D（分享）：分享策略通常针对机会（正面风险），指与第三方合作共同抓住机会并分享收益，例如合资或利益共享协议。题目中针对的是威胁（负面风险），且购买保险等行为是单向转移风险而非共享，因此不适用分享策略。
